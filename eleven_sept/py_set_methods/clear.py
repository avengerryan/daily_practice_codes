# e.g.1: remove all elements from a python set using clear()

# set of vowels
vowels = {'a', 'e', 'i', 'o', 'u'}
print('Vowels (before clear): ', vowels)

# clearing vowels
vowels.clear()
print('Vowels (after clear): ', vowels)






























