
# python set issuperset() : checks if a set is superset of another set
# the issuperset() method returns True if a set has every elements of another
# set (passed as an argument). If not, it returns False.

# A.issuperset(B)





# e.g: How issuperset() works

A = {1, 2, 3, 4, 5}
B = {1, 2, 3}
C = {1, 2, 3}

# returns True
print(A.issuperset(B))

# returns False
print(B.issuperset(A))

# returns True
print(C.issuperset(B))






































