
# python string partition() : returns a tuple
# the partition() method splits the string at the first occurrence of the argument
# string and returns a tuple containing the part the before separator, argument
# string and the part after the separator.

# string.partition(separator)








# e.g. How partition() works
string = "Python is fun"

# 'is' separator is found
print(string.partition('is '))

# 'not' separator is not found
print(string.partition('not '))

string = "Python is fun, isn't it"

# splits at first occurrence of 'is'
print(string.partition('is '))












































































































