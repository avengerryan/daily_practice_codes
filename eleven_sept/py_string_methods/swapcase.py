
# python string swapcase() : swap uppercase characters to lowercase, vice versa
# the string swapcase() method converts all uppercase characters to lowercase
# characters to uppercase characters of the given string, and returns it.

# string.swapcase()







# e.g.1: swap lowercase to uppercase and vice vera using swapcase()

# e.g. string
string = "THIS SHOULD ALL BE LOWERCASE."
print(string.swapcase())

string = "this should all be uppercase."
print(string.swapcase())

