
# Python bool()
# bool() method converts a value to Boolean (True or False) using the standard truth testing procedure.


# syntax: bool([value])


test = []
print(test, 'is', bool(test))

test = [0]
print(test, 'is', bool(test))

test = 0.0
print(test, 'is', bool(test))

# None is considered False in Python
test = None
print(test, 'is', bool(test))

test = True
print(test, 'is', bool(test))

test = 'Easy string'
print(test, 'is', bool(test))

























