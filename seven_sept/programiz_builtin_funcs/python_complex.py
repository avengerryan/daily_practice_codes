
# python complex()
# the complex() method returns a complex no. when real and imaginary parts are
# provided, or it converts a string to a complex no.

# syntax: complex([real[, imag]])



# creating a complex no. in Python


z = complex(2, -3)
print(z)

z = complex(1)
print(z)

z = complex()
print(z)

z = complex('5-9j')
print(z)


























