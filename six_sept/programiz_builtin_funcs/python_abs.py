
# python abs()
# the abs() method returns the absolute value of the given no.
# if the no. is the complex no., abs() returns its magnitude.


# syntax: abs(num)

# abs() method takes a single argument:
# num: a no. whose absolute value is to be returned.
# the no. can be - integer, floating no., complex no.


# example: get absolute value of a no.

# random integer
integer = -20
print('Absolute value of -20 is:', abs(integer))

# random floating no.
floating = -30.33
print('Absolute value of -30.33 is:', abs(floating))



# get magnitude of a complex no.
complex = (3 - 4j)
print('Magnitude of 3 - 4j is:', abs(complex))



















